#include "../includes/ft_printf.h"

char		ft_cinset(char c, const char *set)
{
	int i;

	if (set != NULL)
	{
		i = 0;
		while (set[i])
		{
			if (c == set[i])
				return (c);
			i++;
		}
		return (0);
	}
	return (-1);
}
