#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char *copy;

	if (s1 != NULL)
	{
		if (!(copy = (char *)malloc((ft_strlen(s1) + 1) * sizeof(char))))
			return (NULL);
		ft_memmove(copy, s1, ft_strlen(s1) + 1);
		return (copy);
	}
	return (NULL);
}
