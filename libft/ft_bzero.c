#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*buffer;
	size_t			i;

	if (s != NULL)
	{
		i = 0;
		buffer = (unsigned char*)s;
		while (i < n)
		{
			buffer[i] = 0;
			i++;
		}
	}
}
