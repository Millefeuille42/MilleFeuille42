#include "libft.h"

void	ft_lstadd_back(t_list **alst, t_list *new)
{
	t_list	*cur;

	if (new && alst && *alst)
	{
		cur = *alst;
		while (cur->next != NULL)
			cur = cur->next;
		cur->next = new;
	}
	if (!alst || !*alst)
		*alst = new;
}
