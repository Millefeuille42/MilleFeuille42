#include "libft.h"

char	*ft_substr(const char *s, unsigned int start, size_t len)
{
	char	*str;
	size_t	i;

	if (s != NULL)
	{
		i = 0;
		if (start >= ft_strlen(s) || !(*s) || len == 0)
			return (ft_strdup("\0"));
		if (!(str = (char *)ft_calloc(len + 1, sizeof(char))))
			return (NULL);
		while (s[start + i] && i < len)
		{
			str[i] = s[start + i];
			i++;
		}
		str[i] = '\0';
		return (str);
	}
	return (ft_strdup("\0"));
}
